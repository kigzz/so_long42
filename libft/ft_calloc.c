#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = (void *)malloc(size * count);
	if (ptr != NULL)
		ft_bzero(ptr, size * count);
	else
		return (NULL);
	return (ptr);
}
